void MarginSocket::SendCrypted( EncryptedPacket &cryptedPacket )
{
	if (TFEncrypt.get() != NULL)
	{
		SendPacket(TCPVariableLengthPacket(cryptedPacket.toCipherText(TFEncrypt.get())));
	}
}

